// --- 五格計算 ---

/**
 * 五格を計算する
 * @param seiStrokes 姓の各文字の画数配列
 * @param meiStrokes 名の各文字の画数配列
 */
export function calcGokaku(seiStrokes: number[], meiStrokes: number[]): Gokaku {
  const seiTotal = seiStrokes.reduce((a, b) => a + b, 0);
  const meiTotal = meiStrokes.reduce((a, b) => a + b, 0);
  const soukaku = seiTotal + meiTotal;

  // 天格: 姓の総画数
  // 一文字姓の場合は仮成数（+1）を加えない流派もあるが、熊崎式ではそのまま
  const tenkaku = seiTotal;

  // 地格: 名の総画数
  // 一文字名の場合も同様にそのまま
  const chikaku = meiTotal;

  // 人格: 姓の末尾 + 名の先頭
  const jinkaku = seiStrokes[seiStrokes.length - 1] + meiStrokes[0];

  // 外格: 総格 - 人格
  // ただし1になる場合（一文字姓+一文字名）は2とする流派もあるが、ここは素直に計算
  let gaikaku = soukaku - jinkaku;
  if (gaikaku <= 0) gaikaku = 1;

  return { tenkaku, jinkaku, chikaku, gaikaku, soukaku };
}

// --- 三才配置 ---

// 五行の型
export type Gogyo = "木" | "火" | "土" | "金" | "水";

export interface SansaiData {
  tenchi: [Gogyo, Gogyo, Gogyo]; // 天・人・地の五行
  kikkyo: string;                // 吉凶判定
  description: string;           // 解説
}

/**
 * 画数の下一桁から五行を求める
 * 1,2=木 / 3,4=火 / 5,6=土 / 7,8=金 / 9,0=水
 */
export function kakuToGogyo(kaku: number): Gogyo {
  const mod = kaku % 10;
  if (mod === 1 || mod === 2) return "木";
  if (mod === 3 || mod === 4) return "火";
  if (mod === 5 || mod === 6) return "土";
  if (mod === 7 || mod === 8) return "金";
  return "水"; // 9, 0
}

/**
 * 三才配置を求める（天格・人格・地格の五行の組み合わせ）
 */
export function calcSansai(gokaku: Gokaku): SansaiData {
  const ten = kakuToGogyo(gokaku.tenkaku);
  const jin = kakuToGogyo(gokaku.jinkaku);
  const chi = kakuToGogyo(gokaku.chikaku);

  const key = `${ten}${jin}${chi}`;
  const result = SANSAI_TABLE[key];

  if (result) {
    return { tenchi: [ten, jin, chi], ...result };
  }

  // テーブルに該当がない場合のフォールバック
  return {
    tenchi: [ten, jin, chi],
    kikkyo: "吉凶混合",
    description: "複合的な要素を持つ配置です。他の格の吉凶を総合的に判断してください。",
  };
}

