// 数値から該当する詳細データを取得するユーティリティ
export function getSuri81Detail(num: number): Suri81Detail | undefined {
  // 81を超える場合は1-81に正規化
  const normalized = num <= 81 ? num : ((num - 1) % 81) + 1;
  return SURI_81.find((s) => s.number === normalized);
}
