export interface KakusuLookupResult {
  char: string;
  kakusu: number | null;
  source: "kanji" | "hiragana" | "katakana" | "unknown";
}

export function getCharKakusu(char: string): KakusuLookupResult {
  if (KANJI_KAKUSU[char] !== undefined) {
    return { char, kakusu: KANJI_KAKUSU[char], source: "kanji" };
  }
  if (HIRAGANA_KAKUSU[char] !== undefined) {
    return { char, kakusu: HIRAGANA_KAKUSU[char], source: "hiragana" };
  }
  if (KATAKANA_KAKUSU[char] !== undefined) {
    return { char, kakusu: KATAKANA_KAKUSU[char], source: "katakana" };
  }
  return { char, kakusu: null, source: "unknown" };
}

export interface StrokesResult {
  strokes: number[];
  unknownChars: string[];
}

export function getStrokes(text: string): StrokesResult {
  const strokes: number[] = [];
  const unknownChars: string[] = [];

  for (const char of text) {
    const result = getCharKakusu(char);
    if (result.kakusu !== null) {
      strokes.push(result.kakusu);
    } else {
      unknownChars.push(char);
      strokes.push(0);
    }
  }

  return { strokes, unknownChars };
}
