/**
 * 六十干支インデックス（0〜59）から納音データを返す。
 * 連続する2つの干支が同じ納音を共有するため、floor(index / 2) で引く。
 */
export function getNacchin(kanshiIndex: number): NacchinData {
  const key = Math.floor(kanshiIndex / 2);
  const data = NACCHIN_TABLE[key];
  if (!data) {
    throw new Error(
      `Invalid kanshiIndex: ${kanshiIndex} (expected 0-59, derived key=${key})`
    );
  }
  return data;
}
