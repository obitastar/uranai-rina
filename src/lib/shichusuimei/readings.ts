/**
 * 鑑定文を取得
 */
export function getReadings(params: ReadingParams): {
  essence: string;
  love: string;
  work: string;
  yearly: string;
} {
  const { nicchu, zokanTsuhensei, tpiMonth, currentYearTsuhensei, gender } = params;

  const loveData = LOVE_READINGS[zokanTsuhensei];
  const love = gender === 'male' ? loveData.male : loveData.female;

  return {
    essence: ESSENCE_READINGS[nicchu],
    love,
    work: WORK_READINGS[tpiMonth],
    yearly: YEARLY_READINGS[currentYearTsuhensei],
  };
}
